import javax.swing.*;

public class ChessCoordinate{

        private int x, y;

        public ChessCoordinate(int x, int y) {
            this.x = x;
            this.y = y;
        }

        public void resetChessCoordinate(int x,int y){
            this.x = x;
            this.y = y;
        }

        public int getX() {
            return x;
        }

        public int getY() {
            return y;
        }





}
